package college;
public class Address {
    int houseNo;
    String street;
    String landmark;
    String city;
    String state;
    int pincode;

    Address(int houseNo, String street, String landmark, String city, String state, int pincode){
        this.houseNo=houseNo;
        this.street=street;
        this.landmark=landmark;
        this.city=city;
        this.state=state;
        this.pincode =pincode;
    }
}
